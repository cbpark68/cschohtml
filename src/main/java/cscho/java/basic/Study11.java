package cscho.java.basic;

public class Study11 {

	public static void main(String[] args) {
		loop10();
		System.out.println();
		loop20();
	}

	public static void loop10() {
		//for(int i=1;i<=10;i++) {
		int i = 1;
		while(i<=10) {
			System.out.print(i+" ");
			i++;
		}
	}

	public static void loop20() {
		//for(int i=1;i<=20;i++) {
		int i = 1;
		while(i<=20) {
			System.out.print(i+" ");
			i++;
		}
	}
}
